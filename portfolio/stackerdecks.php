                            <p>I designed and built <a href="http://Stackerdecks.com/" target="_blank">Stackerdecks.com</a>, a complex, responsive piece of tech, from original concept, design artwork and wireframes, to the launch of multiple products across web and mobile.
Stackerdecks is a social media platform and cutting-edge slideshow technology.
Stackerdecks allows users to collect any social media post, blog post, product page, memes, animated GIF and more into a slideshow, using a simple bookmarklet, or Chrome/Safari extension.
Stackerdecks are embeddable across the web and sharable across multiple social media platforms.
Built and implemented all UI/UX.
Designed all web/print product and marketing collateral.
Managed, software engineers, server administrators, and colleagues.
